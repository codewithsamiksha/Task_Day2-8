fruits = {'apple', 'banana', 'mango'}
#a) Add 'orange' using add().
print(fruits.add('orange'))
print("Adding Orange in Fruits = ",fruits)

#b) Add 'banana' again (observe the result).
print(fruits.add('banana'))
print("Again Add Banana in Fruits = ",fruits)

#c) Remove 'mango' using remove().
print(fruits.remove('mango'))
print("Remove Mange from Fruits = ",fruits)

#d) Try to remove 'grape' using discard() (no error should occur).
print(fruits.discard('grape'))
print("Discard Grape from Fruits",fruits)
