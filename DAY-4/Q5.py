import math
a = int(input("Enter a Number :-"))             #take input from user
print("square=" , math.sqrt(a))                  #give square of no.
print("Factorial=" , math.factorial(a))      #give factorialvalue of no.
print("Ceiling=" , math.ceil(a))        #give ceil value of no.
print("Floor=" , math.floor(a))         #give floor value of no.